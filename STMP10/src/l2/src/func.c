uchar * left_trim(uchar *ptr, uchar value, unsigned int len)
{
	uchar *ptmp;
	unsigned int i;

	ptmp = ptr;
	for (i=0; i<len; i++)
	{
		if (ptmp[i] != value)
			break;
	}
	return(ptmp+i);
}



void DataConvAmt(uchar *disp_buf, uchar *amt_buf, uchar len)
{
	uchar i,j,new_len;
	uchar *ptmp;

	j=0;

 // disp_buf[1]='$';/* +- sign */
 // j = 1;
	ptmp = left_trim(amt_buf, '0', len);
	new_len = len - (uchar)(ptmp - amt_buf);
	amt_buf = ptmp;

	disp_buf[j+2]='.';
	disp_buf[j+5]=0x0;
	switch(new_len)
	{
		case 0: 
			disp_buf[j+1]='0';
			disp_buf[j+3]='0';
			disp_buf[j+4]='0';
			break;
		case 1: 
			disp_buf[j+1]='0';
			disp_buf[j+3]='0';
			disp_buf[j+4]=*amt_buf;
			break;
		case 2: 
			disp_buf[j+1]='0';
			disp_buf[j+3]=*amt_buf;
			disp_buf[j+4]=*(amt_buf+1);
			break;
		default:
			for(i=0;i<(new_len-2);i++)
				disp_buf[j+i+1]=*(amt_buf+i);
			disp_buf[j+new_len-1]='.';
			disp_buf[j+new_len]=*(amt_buf+new_len-2);
			disp_buf[j+new_len+1]=*(amt_buf+new_len-1);
			disp_buf[j+new_len+1+1]='\0';
			break;
	}
	disp_buf[0] = (new_len<3)? (4+j) :(new_len+1+j);
	disp_buf[disp_buf[0]+1] = 0x0;
}


