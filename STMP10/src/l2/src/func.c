/*********************************************************************
 NAME: void dat_bcdtoasc( uchar *asc, uchar *bcd, unsigned short asc_len )
 FUNC: convert bcd data to ascii data
 IN  : bcd: bcd data point
       asc_len:the length of result ascii data
 OUT : asc: ascii data point
 RTRN: 
 NOTE: for example: 
        bcd ="\x11\x22\x33"
        dat_bcdtoasc(asc,bcd,6)
        so asc="112233"
*********************************************************************/
void dat_bcdtoasc( uchar  *asc, uchar  *bcd, unsigned short asc_len )
{
   uchar  is_first;
   uchar   by;

   is_first =(asc_len % 2); //change by wxk 98.11.06

   while (asc_len-->0)
     {

     if (is_first){
       by = *bcd & 0x0f;
       bcd++;
     }
     else
       {
       by = *bcd >> 4;
       }
     by += (by >= 0x0a) ? 0x37 : '0';       /* 0x37 = 'A' - 0x0a*/

     *asc++ = by;
     is_first = !is_first;
     }
   *asc =0;
}

